public class VariablesOperatorsOpdracht6 {
    public static void main(String[] args) {
        int cats = 5;
        int dogs = 4;
        System.out.println(cats > dogs);
        System.out.println(cats < dogs);
        System.out.println(cats <= dogs);
        System.out.println(cats >= dogs);
        System.out.println(cats == dogs);
        System.out.println(cats != dogs);
    }
}
