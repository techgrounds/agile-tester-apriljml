public class VariablesOperatorsOpdracht6 {
    public static void main(String[] args) {
        int cats = 5;
        int dogs = 4;
        System.out.print("groter dan: ");
        System.out.println(cats > dogs);
        System.out.print("kleiner dan: ");
        System.out.println(cats < dogs);
        System.out.print("kleiner of gelijk aan: ");
        System.out.println(cats <= dogs);
        System.out.print("groter of gelijk aan: ");
        System.out.println(cats >= dogs);
        System.out.print("gelijk aan: ");
        System.out.println(cats == dogs);
        System.out.print("niet gelijk aan: ");
        System.out.println(cats != dogs);
    }
}
